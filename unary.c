//program for unery operator
#include<stdio.h>
int main()
{
    int a = 9;
    int b = 5;
   
    printf("%d\n",++a);//value increments by 1
    printf("%d\n",++b);//value increments by 1
    printf("%d\n",--b);//value decrements by 1
    printf("%d\n",--a);//value decrements by 1
    return 0;
    
}